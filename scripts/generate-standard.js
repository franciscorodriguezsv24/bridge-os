#!/usr/bin/env node
// =============================================================================
// Bridge OS вЂ” generate-standard.js
// Reads Design OS export and generates design-system.md for Agent OS.
// Usage: node generate-standard.js --export <path> --output <path>
// =============================================================================

const fs   = require("fs");
const path = require("path");

// в”Ђв”Ђ Args в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const args       = process.argv.slice(2);
const exportPath = args[args.indexOf("--export") + 1];
const outputPath = args[args.indexOf("--output") + 1];

if (!exportPath || !outputPath) {
  console.error("Usage: node generate-standard.js --export <path> --output <path>");
  process.exit(1);
}

if (!fs.existsSync(exportPath)) {
  console.error(`вњ— Export path not found: ${exportPath}`);
  process.exit(1);
}

// в”Ђв”Ђ Helpers в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

// Safely read a JSON file вЂ” returns null if missing or invalid
function readJSON(filePath) {
  try {
    return JSON.parse(fs.readFileSync(filePath, "utf8"));
  } catch {
    return null;
  }
}

// Safely read a text file вЂ” returns empty string if missing
function readFile(filePath) {
  try {
    return fs.readFileSync(filePath, "utf8");
  } catch {
    return "";
  }
}

// List immediate subdirectories of a folder
function listDirs(dirPath) {
  if (!fs.existsSync(dirPath)) return [];
  return fs.readdirSync(dirPath).filter(f =>
    fs.statSync(path.join(dirPath, f)).isDirectory()
  );
}

// в”Ђв”Ђ 1. Read Design Tokens в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
// Design OS exports tokens as CSS files, not JSON:
//   design-system/tokens.css        вЂ” CSS custom properties
//   design-system/tailwind-colors.css вЂ” color tokens
//   design-system/fonts.md          вЂ” typography info

function parseTokens(tokensDir) {
  if (!fs.existsSync(tokensDir)) return null;

  const tokensCss  = readFile(path.join(tokensDir, "tokens.css"));
  const colorsCss  = readFile(path.join(tokensDir, "tailwind-colors.css"));
  const fontsMd    = readFile(path.join(tokensDir, "fonts.md"));

  return { tokensCss, colorsCss, fontsMd };
}

// Parse CSS custom properties from a CSS string
// Looks for lines like: --color-primary: #6366F1;
function parseCSSTokens(css) {
  const rows = [];
  if (!css) return rows;
  const regex = /(--[\w-]+)\s*:\s*([^;]+);/g;
  let match;
  while ((match = regex.exec(css)) !== null) {
    rows.push({ token: match[1].trim(), value: match[2].trim() });
  }
  return rows;
}

// в”Ђв”Ђ 2. Read Components в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
function parseComponents(componentsDir) {
  const components = [];
  const dirs = listDirs(componentsDir);

  for (const name of dirs) {
    const compDir  = path.join(componentsDir, name);
    const specPath = path.join(compDir, "spec.md");
    const spec     = readFile(specPath);

    // Extract variants from spec if present (looks for a Variants section)
    const variantsMatch = spec.match(/##\s*Variants[\s\S]*?\n([\s\S]*?)(?=\n##|$)/i);
    const variants = variantsMatch
      ? variantsMatch[1]
          .split("\n")
          .map(l => l.replace(/^\|?\s*`?([^|`]+)`?\s*\|?.*/, "$1").trim())
          .filter(l => l && !l.startsWith("-") && !l.startsWith("Variant"))
          .slice(0, 6)
      : [];

    // Extract props similarly
    const propsMatch = spec.match(/##\s*Props[\s\S]*?\n([\s\S]*?)(?=\n##|$)/i);
    const props = propsMatch
      ? propsMatch[1]
          .split("\n")
          .map(l => l.replace(/^\|?\s*`?([^|`\s]+)`?.*/, "$1").trim())
          .filter(l => l && !l.startsWith("-") && !l.startsWith("Prop") && l.length > 1)
          .slice(0, 8)
      : [];

    components.push({ name, variants, props, hasSpec: !!spec });
  }

  return components;
}

// в”Ђв”Ђ 3. Build design-system.md в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
function buildStandard({ tokens, components, exportPath }) {
  const timestamp = new Date().toISOString().split("T")[0];
  const lines     = [];

  // в”Ђв”Ђ Header
  lines.push(`# Design System Standards`);
  lines.push(`>`);
  lines.push(`> Auto-generated by Bridge OS`);
  lines.push(`> Source: ${exportPath}`);
  lines.push(`> Last sync: ${timestamp}`);
  lines.push(`>`);
  lines.push(`> вљ пёЏ Do not edit this file manually вЂ” run \`.bridge-os/sync.sh\` to update.`);
  lines.push(``);
  lines.push(`---`);
  lines.push(``);

  // в”Ђв”Ђ Agent Rules (most important вЂ” read first)
  lines.push(`## Rules for the Agent`);
  lines.push(``);
  lines.push(`1. **Tokens only** вЂ” Never hardcode color, spacing, or typography values.`);
  lines.push(`   Always use the CSS variables defined in the tokens table below.`);
  lines.push(`2. **Use exported components** вЂ” If a component exists in \`design-export/components/\`,`);
  lines.push(`   import and use it. Do not re-implement it.`);
  lines.push(`3. **Check user flows first** вЂ” Before implementing any screen, read`);
  lines.push(`   \`design-export/user-flows.md\` to understand the correct sequence and states.`);
  lines.push(`4. **Shell is the layout** вЂ” The app shell from \`design-export/components/Shell/\``);
  lines.push(`   wraps all sections. Do not create custom layout wrappers.`);
  lines.push(`5. **No new components without a spec** вЂ” If a component doesn't exist in`);
  lines.push(`   \`design-export/\`, stop and ask before creating it.`);
  lines.push(``);
  lines.push(`---`);
  lines.push(``);

  // в”Ђв”Ђ Design Tokens
  lines.push(`## Design Tokens`);
  lines.push(``);

  const allTokens    = parseCSSTokens(tokens?.tokensCss);
  const colorTokens  = parseCSSTokens(tokens?.colorsCss);

  if (colorTokens.length > 0) {
    lines.push(`### Colors`);
    lines.push(``);
    lines.push(`| Token | Value |`);
    lines.push(`|-------|-------|`);
    for (const row of colorTokens) {
      lines.push(`| \`${row.token}\` | \`${row.value}\` |`);
    }
    lines.push(``);
  }

  if (allTokens.length > 0) {
    lines.push(`### Tokens`);
    lines.push(``);
    lines.push(`| Token | Value |`);
    lines.push(`|-------|-------|`);
    for (const row of allTokens) {
      lines.push(`| \`${row.token}\` | \`${row.value}\` |`);
    }
    lines.push(``);
  }

  if (tokens?.fontsMd) {
    lines.push(`### Typography`);
    lines.push(``);
    lines.push(tokens.fontsMd.trim());
    lines.push(``);
  }

  if (!colorTokens.length && !allTokens.length && !tokens?.fontsMd) {
    lines.push(`_No token files found in \`design-export/design-system/\`._`);
    lines.push(``);
  }

  lines.push(`---`);
  lines.push(``);

  // в”Ђв”Ђ Components
  lines.push(`## Available Components`);
  lines.push(``);
  lines.push(`The following components are exported in \`design-export/components/\`.`);
  lines.push(`**Import them вЂ” do not recreate them.**`);
  lines.push(``);

  if (components.length === 0) {
    lines.push(`_No components found in \`design-export/components/\`._`);
  } else {
    for (const comp of components) {
      lines.push(`### ${comp.name}`);
      lines.push(``);
      lines.push(`- **Path:** \`design-export/components/${comp.name}/\``);
      if (comp.variants.length > 0) {
        lines.push(`- **Variants:** ${comp.variants.map(v => `\`${v}\``).join(" | ")}`);
      }
      if (comp.props.length > 0) {
        lines.push(`- **Key props:** ${comp.props.map(p => `\`${p}\``).join(", ")}`);
      }
      if (comp.hasSpec) {
        lines.push(`- **Spec:** \`design-export/components/${comp.name}/spec.md\``);
      }
      lines.push(``);
    }
  }

  lines.push(`---`);
  lines.push(``);

  // в”Ђв”Ђ Reference files
  lines.push(`## Design OS Reference Files`);
  lines.push(``);
  lines.push(`| File | When to use |`);
  lines.push(`|------|-------------|`);
  lines.push(`| \`design-export/prompts/one-shot-prompt.md\` | Full implementation in one session |`);
  lines.push(`| \`design-export/prompts/section-prompt.md\` | Implement section by section |`);
  lines.push(`| \`design-export/shell/\` | App shell and navigation components |`);
  lines.push(`| \`design-export/sections/\` | Section components with test specs |`);
  lines.push(`| \`design-export/product-overview.md\` | Product context and requirements |`);
  lines.push(``);

  return lines.join("\n");
}

// в”Ђв”Ђ Main в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
// Design OS real structure: design-system/ has tokens, sections/ has components
const tokensDir     = path.join(exportPath, "design-system");
const componentsDir = path.join(exportPath, "sections");

const tokens     = parseTokens(tokensDir);
const components = parseComponents(componentsDir);
const standard   = buildStandard({ tokens, components, exportPath });

// Write output
const outputDir = path.dirname(outputPath);
if (!fs.existsSync(outputDir)) fs.mkdirSync(outputDir, { recursive: true });
fs.writeFileSync(outputPath, standard, "utf8");

const colorCount = parseCSSTokens(tokens?.colorsCss).length;
const tokenCount = parseCSSTokens(tokens?.tokensCss).length;
console.log(`   вњ“ design-system.md written (${components.length} components, ${colorCount} colors, ${tokenCount} tokens)`);