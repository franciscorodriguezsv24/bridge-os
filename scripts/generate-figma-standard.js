#!/usr/bin/env node
// =============================================================================
// Bridge OS — generate-figma-standard.js
// Reads .bridge-os/figma-tokens.json and generates design-system.md for Agent OS.
// Usage: node generate-figma-standard.js --tokens <path> --output <path>
// =============================================================================

const fs   = require("fs");
const path = require("path");

// ── Args ──────────────────────────────────────────────────────────────────────
const args       = process.argv.slice(2);
const tokensPath = args[args.indexOf("--tokens") + 1];
const outputPath = args[args.indexOf("--output") + 1];

if (!tokensPath || !outputPath) {
  console.error("Usage: node generate-figma-standard.js --tokens <path> --output <path>");
  process.exit(1);
}

if (!fs.existsSync(tokensPath)) {
  console.error(`✗ Figma tokens not found: ${tokensPath}`);
  process.exit(1);
}

// ── Load tokens ───────────────────────────────────────────────────────────────
let figmaTokens;
try {
  figmaTokens = JSON.parse(fs.readFileSync(tokensPath, "utf8"));
} catch (e) {
  console.error(`✗ Could not parse ${tokensPath}: ${e.message}`);
  process.exit(1);
}

const {
  fileName   = "Unknown Figma File",
  fileKey    = "n/a",
  syncedAt   = new Date().toISOString(),
  colors     = [],
  typography = [],
  spacing    = [],
  other      = []
} = figmaTokens;

// ── Build design-system.md ────────────────────────────────────────────────────
function buildStandard() {
  const timestamp = syncedAt.split("T")[0];
  const lines     = [];

  // Header
  lines.push(`# Design System Standards`);
  lines.push(`>`);
  lines.push(`> Auto-generated by Bridge OS — Figma MCP`);
  lines.push(`> Source: ${fileName} (${fileKey})`);
  lines.push(`> Last sync: ${timestamp}`);
  lines.push(`>`);
  lines.push(`> ⚠️ Do not edit this file manually — re-run \`.bridge-os/sync.sh --figma\` to update.`);
  lines.push(``);
  lines.push(`---`);
  lines.push(``);

  // Agent Rules
  lines.push(`## Rules for the Agent`);
  lines.push(``);
  lines.push(`1. **Tokens only** — Never hardcode color, spacing, or typography values.`);
  lines.push(`   Always use the CSS variables defined in the tokens tables below.`);
  lines.push(`2. **Figma is the source of truth** — All design decisions come from the Figma file.`);
  lines.push(`   If a value is not in this file, ask before inventing it.`);
  lines.push(`3. **Check product docs first** — Before implementing any screen, read`);
  lines.push(`   \`agent-os/product/design-requirements.md\` and \`agent-os/product/roadmap.md\`.`);
  lines.push(`4. **Use CSS variables** — Always reference tokens via their CSS variable name`);
  lines.push(`   (e.g. \`var(--color-primary-blue)\`), never the raw hex value.`);
  lines.push(`5. **No new design decisions** — If a color or spacing value is not in this file,`);
  lines.push(`   stop and ask before adding it. The Figma file is the only authority.`);
  lines.push(``);
  lines.push(`---`);
  lines.push(``);

  // Colors
  if (colors.length > 0) {
    lines.push(`## Colors`);
    lines.push(``);
    lines.push(`| CSS Variable | Value | Figma Name |`);
    lines.push(`|-------------|-------|------------|`);
    for (const c of colors) {
      lines.push(`| \`${c.cssVar}\` | \`${c.value}\` | ${c.name} |`);
    }
    lines.push(``);
  }

  // Typography
  if (typography.length > 0) {
    lines.push(`## Typography`);
    lines.push(``);
    lines.push(`| CSS Variable | Value | Figma Name |`);
    lines.push(`|-------------|-------|------------|`);
    for (const t of typography) {
      lines.push(`| \`${t.cssVar}\` | \`${t.value}\` | ${t.name} |`);
    }
    lines.push(``);
  }

  // Spacing
  if (spacing.length > 0) {
    lines.push(`## Spacing`);
    lines.push(``);
    lines.push(`| CSS Variable | Value | Figma Name |`);
    lines.push(`|-------------|-------|------------|`);
    for (const s of spacing) {
      lines.push(`| \`${s.cssVar}\` | \`${s.value}\` | ${s.name} |`);
    }
    lines.push(``);
  }

  // Other tokens
  if (other.length > 0) {
    lines.push(`## Other Tokens`);
    lines.push(``);
    lines.push(`| CSS Variable | Value | Figma Name |`);
    lines.push(`|-------------|-------|------------|`);
    for (const o of other) {
      lines.push(`| \`${o.cssVar}\` | \`${o.value}\` | ${o.name} |`);
    }
    lines.push(``);
  }

  if (!colors.length && !typography.length && !spacing.length && !other.length) {
    lines.push(`_No tokens found in figma-tokens.json._`);
    lines.push(`_Re-run \`/bridge-design\` → Step A-2 to pull tokens from Figma._`);
    lines.push(``);
  }

  lines.push(`---`);
  lines.push(``);

  // Figma reference
  lines.push(`## Figma Reference`);
  lines.push(``);
  lines.push(`| Property | Value |`);
  lines.push(`|----------|-------|`);
  lines.push(`| File | ${fileName} |`);
  lines.push(`| File Key | \`${fileKey}\` |`);
  lines.push(`| Last Pulled | ${timestamp} |`);
  lines.push(`| Tokens Source | Figma Variables API (via MCP) |`);
  lines.push(``);
  lines.push(`To update tokens from Figma, run:`);
  lines.push(`1. \`/bridge-design\` → choose Figma MCP → Step A-2 to re-pull variables`);
  lines.push(`2. \`.bridge-os/sync.sh --figma\` to regenerate this file`);
  lines.push(``);

  return lines.join("\n");
}

// ── Write output ──────────────────────────────────────────────────────────────
const standard  = buildStandard();
const outputDir = path.dirname(outputPath);
if (!fs.existsSync(outputDir)) fs.mkdirSync(outputDir, { recursive: true });
fs.writeFileSync(outputPath, standard, "utf8");

console.log(
  `   ✓ design-system.md written from Figma (${colors.length} colors, ` +
  `${typography.length} typography, ${spacing.length} spacing tokens)`
);
